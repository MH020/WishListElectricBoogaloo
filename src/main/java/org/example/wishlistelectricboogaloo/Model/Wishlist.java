package org.example.wishlistelectricboogaloo.Model;

public class Wishlist {
   private int id;
   private String City;

   public Wishlist() {
   }

   public int getId() {
      return id;
   }
    public void setId(int id) {
        this.id = id;
    }
    public String getCity() {
        return City;
    }
    public void setCity(String City) {
        this.City = City;
    }
}
